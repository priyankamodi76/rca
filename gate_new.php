	  <h1 align="center" > GATE SYLLABUS 2020 (CIVIL ENGINEERING) </h1>
	  <h2 align="center" >Section 1: Engineering Mathematics</h2>
	  
	  <h3> Linear Algebra:</h3>
      <p align="justify">  Matrix algebra; Systems of linear equations; Eigen values and Eigen vectors.</p>
	  
	  <h3> Calculus:</h3>
      <p align="justify">  Functions of single variable; Limit, continuity and differentiability; Mean value theorems, local maxima and minima, Taylor
and Maclaurin series; Evaluation of definite and indefinite integrals, application of definite integral to obtain area and
volume; Partial derivatives; Total derivative; Gradient, Divergence and Curl, Vector identities, Directional derivatives, Line,
Surface and Volume integrals, Stokes, Gauss and Greenâ€™s theorems.</p>

 <h3> Ordinary Differential Equation (ODE):</h3>
      <p align="justify"> First order (linear and non-linear) equations; higher order linear equations with constant coefficients; Euler-Cauchy
equations; Laplace transform and its application in solving linear ODEs; initial and boundary value problems.</p>

  <h3> Partial Differential Equation (PDE):</h3>
      <p align="justify">  Fourier series; separation of variables; solutions of one-dimensional diffusion equation; first and second order one-
dimensional wave equation and two-dimensional Laplace equation.</p>

 <h3> Probability and Statistics:</h3>
      <p align="justify"> Definitions of probability and sampling theorems; Conditional probability; Discrete Random variables: Poisson and
Binomial distributions; Continuous random variables: normal and exponential distributions; Descriptive statistics - Mean,
median, mode and standard deviation; Hypothesis testing.</p>

<h2 align="center">Section 2: Structural Engineering<h2>
 <h3>Engineering Mechanics:</h3>
      <p align="justify"> System of forces, free-body diagrams, equilibrium equations; Internal forces in structures; Friction and its applications;
Kinematics of point mass and rigid body; Centre of mass; Eulerâ€™s equations of motion; Impulse-momentum; Energy
methods; Principles of virtual work.</p>

 <h3>Solid Mechanics:</h3>
      <p align="justify"> Bending moment and shear force in statically determinate beams; Simple stress and strain relationships; Theories of
failures; Simple bending theory, flexural and shear stresses, shear centre; Uniform torsion, buckling of column, combined
and direct bending stresses.</p>


 <h3>Structural Analysis:</h3>
      <p align="justify">Statically determinate and indeterminate structures by force/ energy methods; Method of superposition; Analysis of
trusses, arches, beams, cables and frames; Displacement methods: Slope deflection and moment distribution methods;
Influence lines; Stiffness and flexibility methods of structural analysis.</p>


 <h3>Construction Materials and Management:</h3>
      <p align="justify">Construction Materials:Structural steel - composition, material properties and behaviour; Concrete - constituents, mix
design, short-term and long-term properties; Bricks and mortar; Timber; Bitumen. Construction Management: Types of
construction projects; Tendering and construction contracts; Rate analysis and standard specifications; Cost estimation;
Project planning and network analysis - PERT and CPM.</p>

 <h3>Concrete Structures:</h3>
      <p align="justify">Working stress, Limit state and Ultimate load design concepts; Design of beams, slabs, columns; Bond and development
length; Prestressed concrete; Analysis of beam sections at transfer and service loads.</p>

 <h3>Steel Structures:</h3>
      <p align="justify">Working stress and Limit state design concepts; Design of tension and compression members, beams and beamcolumns, column bases; Connections â€“ simple and eccentric, beam-column connections, plate girders and trusses;
Plastic analysis of beams and frames</p>


<h2 align="center">Section 3: Geotechnical Engineering<h2>
 <h3>Soil Mechanics:</h3>
      <p align="justify"> Origin of soils, soil structure and fabric; Three-phase system and phase relationships, index properties; Unified and Indian
standard soil classification system; Permeability - one dimensional flow, Darcyâ€™s law; Seepage through soils - twodimensional flow, flow nets, uplift pressure, piping; Principle of effective stress, capillarity, seepage force and quicksand
condition; Compaction in laboratory and field conditions; One- dimensional consolidation, time rate of consolidation;
Mohrâ€™s circle, stress paths, effective and total shear strength parameters, characteristics of clays and sand.</p>

 <h3>Foundation Engineering:</h3>
      <p align="justify"> Sub-surface investigations - scope, drilling bore holes, sampling, plate load test, standard penetration and cone
penetration tests; Earth pressure theories - Rankine and Coulomb; Stability of slopes - finite and infinite slopes, method of
slices and Bishopâ€™s method; Stress distribution in soils - Boussinesqâ€™s and Westergaardâ€™s theories, pressure bulbs;
Shallow foundations - Terzaghiâ€™s and Meyerhoffâ€™s bearing capacity theories, effect of water table; Combined footing and
raft foundation; Contact pressure; Settlement analysis in sands and clays; Deep foundations - types of piles, dynamic and
static formulae, load capacity of piles in sands and clays, pile load test, negative skin friction.</p>


<h2 align="center">Section 4: Water Resources Engineering<h2>
 <h3>Fluid Mechanics:</h3>
      <p align="justify"> Properties of fluids, fluid statics; Continuity, momentum, energy and corresponding equations; Potential flow, applications
of momentum and energy equations; Laminar and turbulent flow; Flow in pipes, pipe networks; Concept of boundary layer
and its growth.</p>

 <h3>Hydraulics:</h3>
      <p align="justify"> Forces on immersed bodies; Flow measurement in channels and pipes; Dimensional analysis and hydraulic similitude;
Kinematics of flow, velocity triangles; Basics of hydraulic machines, specific speed of pumps and turbines; Channel
Hydraulics - Energy-depth relationships, specific energy, critical flow, slope profile, hydraulic jump, uniform flow and
gradually varied flow</p>

 <h3>Hydrology:</h3>
      <p align="justify">Hydrologic cycle, precipitation, evaporation, evapo-transpiration, watershed, infiltration, unit hydrographs, hydrograph
analysis, flood estimation and routing, reservoir capacity, reservoir and channel routing, surface run-off models, ground
water hydrology - steady state well hydraulics and aquifers; Application of Darcyâ€™s law.</p>

 <h3>Irrigation:</h3>
      <p align="justify">Duty, delta, estimation of evapo-transpiration; Crop water requirements; Design of lined and unlined canals, head works,
gravity dams and spillways; Design of weirs on permeable foundation; Types of irrigation systems, irrigation methods;
Water logging and drainage; Canal regulatory works, cross-drainage structures, outlets and escapes.</p>

<h2 align="center">Section 5: Environmental Engineering<h2>
 <h3>Water and Waste Water:</h3>
      <p align="justify">Quality standards, basic unit processes and operations for water treatment. Drinking water standards, water requirements,
basic unit operations and unit processes for surface water treatment, distribution of water. Sewage and sewerage
treatment, quantity and characteristics of wastewater. Primary, secondary and tertiary treatment of wastewater, effluent
discharge standards. Domestic wastewater treatment, quantity of characteristics of domestic wastewater, primary and
secondary treatment. Unit operations and unit processes of domestic wastewater, sludge disposal.</p>

 <h3>Air Pollution:</h3>
      <p align="justify">Types of pollutants, their sources and impacts, air pollution meteorology, air pollution control, air quality standards and
limits.</p>
 <h3>Municipal Solid Wastes:</h3>
      <p align="justify">Characteristics, generation, collection and transportation of solid wastes, engineered systems for solid waste
management (reuse/ recycle, energy recovery, treatment and disposal).</p>

<h3>Noise Pollution:</h3>
      <p align="justify">Impacts of noise, permissible limits of noise pollution, measurement of noise and control of noise pollution.</p>
	  
<h2 align="center">Section 6: Transportation Engineering<h2>
 <h3>Transportation Infrastructure:</h3>
      <p align="justify">Highway alignment and engineering surveys; Geometric design of highways - cross-sectional elements, sight distances,
horizontal and vertical alignments; Geometric design of railway track; Airport runway length, taxiway and exit taxiway
design.</p> 

<h3>Highway Pavements:</h3>
      <p align="justify">Highway materials - desirable properties and quality control tests; Design of bituminous paving mixes; Design factors for
flexible and rigid pavements; Design of flexible pavement using IRC: 37-2012; Design of rigid pavements using IRC: 58-
2011; Distresses in concrete pavements.</p> 

<h3>Traffic Engineering:</h3>
      <p align="justify">Traffic studies on flow, speed, travel time - delay and O-D study, PCU, peak hour factor, parking study, accident study and
analysis, statistical analysis of traffic data; Microscopic and macroscopic parameters of traffic flow, fundamental
relationships; Control devices, signal design by Websterâ€™s method; Types of intersections and channelization; Highway
capacity and level of service of rural highways and urban roads.</p> 

<h2 align="center">Section 7: Geomatics Engineering</h2>

      <p align="justify">Principles of surveying; Errors and their adjustment; Maps - scale, coordinate system; Distance and angle measurement -
Levelling and trigonometric levelling; Traversing and triangulation survey; Total station; Horizontal and vertical curves.
Photogrammetry - scale, flying height; Remote sensing - basics, platform and sensors, visual image interpretation; Basics
of Geographical information system (GIS) and Geographical Positioning system (GPS).</p>

