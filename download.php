      <div class="span12">
      <p align="justify">  The standard of paper in General Studies and Engineering Aptitude (Preliminary Stage-I Examination) will be such as may be expected of an Engineering/Science Graduate. The standard of papers in other subjects will approximately be that of an Engineering Degree Examination of an Indian University. There will be no practical examination in any of the subjects.
</p>
<p align="justify"><b>General Studies and Engineering Aptitude (Preliminary Examination/Stage-I, Paper I, Objective type, Common to all Candidates) </b></p>

<p align="justify">1.Current issues of national and international importance relating to social, economic and industrial development
</br>2. Engineering Aptitude covering Logical reasoning and Analytical ability
</br> 3. Engineering Mathematics and Numerical Analysis
 </br>4. General Principles of Design, Drawing, Importance of Safety
 </br>5. Standards and Quality practices in production, construction, maintenance and services
</br>6. Basics of Energy and Environment: Conservation, environmental pollution and degradation, Climate Change, Environmental impact
</br> 7. Basics of Project Management
</br>8. Basics of Material Science and Engineering
 </br>9. Information and Communication Technologies (ICT) based tools and their applications in Engineering such as networking, e-governance and technology based education.
</br>10. Ethics and values in Engineering profession.
</br>
</br></p>
<h3>Note:</h3> <p align="justify">The paper in General Studies and Engineering Aptitude will include Knowledge of relevant topics as may be expected from an engineering graduate, without special study. Questions from all the 10 topics mentioned above shall be set. Marks for each Topic may range from 5% to 15% of the total marks in the paper.
</br>
Civil Engineering Contents for syllabi of both the Papers together for Preliminary Examination/Stage-I (objective type Paper–II) and separately for Main/Stage-II Examination (Conventional type Paper-I and Paper – II).

</p>

<h3 align="center">Paper-1</h3>

  <h3>1.Building Materials:</h3>

<p align="justify">Stone, Lime, Glass, Plastics, Steel, FRP, Ceramics, Aluminum, Fly Ash, Basic Admixtures, Timber, Bricks and Aggregates: Classification, properties and selection criteria; Cement: Types, Composition, Properties, Uses, Specifications and various Tests; Lime & Cement Mortars and Concrete: Properties and various Tests; Design of Concrete Mixes: Proportioning of aggregates and methods of mix design.
</p><h3> 2. Solid Mechanics:</h3>
  <p align="justify"> Elastic constants, Stress, plane stress, Strains, plane strain, Mohr’s circle of stress and strain, Elastic theories of failure, Principal Stresses, Bending, Shear and Torsion.
</p>
  <h3>3. Structural Analysis:</h3>
<p align="justify">  Basics of strength of materials, Types of stresses and strains, Bending moments and shear force, concept of bending and shear stresses; Analysis of determinate and indeterminate structures; Trusses, beams, plane frames; Rolling loads, Influence Lines, Unit load method & other methods; Free and Forced vibrations of single degree and multi degree freedom system; Suspended Cables; Concepts and use of Computer Aided Design.
</p>
<h3>  4. Design of Steel Structures:</h3>
   <p align="justify">Principles of Working Stress methods, Design of tension and compression members, Design of beams and beam column connections, built-up sections, Girders, Industrial roofs, Principles of Ultimate load design.
</p>
  <h3> 5. Design of Concrete and Masonry structures:</h3>
  <p align="justify">Limit state design for bending, shear, axial compression and combined forces; Design of beams, Slabs, Lintels, Foundations, Retaining walls, Tanks, Staircases; Principles of pre-stressed concrete design including materials and methods; Earthquake resistant design of structures; Design of Masonry Structure.
</p>
  <h3> 6. Construction Practice, Planning and Management:</h3>
  <p align="justify">Construction - Planning, Equipment, Site investigation and Management including Estimation with latest project management tools and network analysis for different Types of works; Analysis of Rates of various types of works; Tendering Process and Contract Management, Quality Control, Productivity, Operation Cost; Land acquisition; Labour safety and welfare.

</p>



<h3 align="center">Paper-2</h3>

  <h3>1.Flow of Fluids, Hydraulic Machines and Hydro Power:
</h3>

<p align="justify">(a) Fluid Mechanics, Open Channel Flow, Pipe Flow: Fluid properties; Dimensional Analysis and Modeling; Fluid dynamics including flow kinematics and measurements; Flow net; Viscosity, Boundary layer and control, Drag, Lift, Principles in open channel flow, Flow controls. Hydraulic jump; Surges; Pipe networks.
</br>(b) Hydraulic Machines and Hydro power - Various pumps, Air vessels, Hydraulic turbines – types, classifications & performance parameters; Power house – classification and layout, storage, pondage, control of supply.
</p><h3> 2. Hydrology and Water Resources Engineering: </h3>
  <p align="justify"> Hydrological cycle, Ground water hydrology, Well hydrology and related data analysis; Streams and their gauging; River morphology; Flood, drought and their management; Capacity of Reservoirs. Water Resources Engineering : Multipurpose uses of Water, River basins and their potential; Irrigation systems, water demand assessment; Resources - storages and their yields; Water logging, canal and drainage design, Gravity dams, falls, weirs, Energy dissipaters, barrage Distribution works, Cross drainage works and head-works and their design; Concepts in canal design, construction & maintenance; River training, measurement and analysis of rainfall.
</p>
  <h3>3.  Environmental Engineering:</h3>
<p align="justify">  (a) Water Supply Engineering: Sources, Estimation, quality standards and testing of water and their treatment; Rural, Institutional and industrial water supply; Physical, chemical and biological characteristics and sources of water, Pollutants in water and its effects, Estimation of water demand; Drinking water Standards, Water Treatment Plants, Water distribution networks.
</br> (b) Waste Water Engineering: Planning & design of domestic waste water, sewage collection and disposal; Plumbing Systems. Components and layout of sewerage system; Planning & design of Domestic Waste-water disposal system; Sludge management including treatment, disposal and re-use of treated effluents; Industrial waste waters and Effluent Treatment Plants including institutional and industrial sewage management.
</br>(c) Solid Waste Management: 18 Sources & classification of solid wastes along with planning & design of its management system; Disposal system, Beneficial aspects of wastes and Utilization by Civil Engineers. (d) Air, Noise pollution and Ecology: Concepts & general methodology.</p>
<h3>  4.  Geo-technical Engineering and Foundation Engineering :</h3>
   <p align="justify">(a)Geo-technical Engineering: Soil exploration - planning & methods, Properties of soil, classification, various tests and interrelationships; Permeability & Seepage, Compressibility, consolidation and Shearing resistance, Earth pressure theories and stress distribution in soil; Properties and uses of geo-synthetics.
 </br>(b) Foundation Engineering: Types of foundations & selection criteria, bearing capacity, settlement analysis, design and testing of shallow & deep foundations; Slope stability analysis, Earthen embankments, Dams and Earth retaining structures: types, analysis and design, Principles of ground modifications
</p>
  <h3> 5. Surveying and Geology: </h3>
  <p align="justify">(a) Surveying: Classification of surveys, various methodologies, instruments & analysis of measurement of distances, elevation and directions; Field astronomy, Global Positioning System; Map preparation; Photogrammetry; Remote sensing concepts; Survey Layout for culverts, canals, bridges, road/railway alignment and buildings, Setting out of Curves. (b) Geology: Basic knowledge of Engineering geology & its application in projects.
</p>
  <h3> 6. Transportation Engineering: </h3>
  <p align="justify">Highways - Planning & construction methodology, Alignment and geometric design; Traffic Surveys and Controls; Principles of Flexible and Rigid pavements design. Tunneling - Alignment, methods of construction, disposal of muck, drainage, lighting and ventilation. Railways Systems – Terminology, Planning, designs and maintenance practices; track modernization.
</br> Harbours – Terminology, layouts and planning.
 </br>Airports – Layout, planning & design.

</p>






